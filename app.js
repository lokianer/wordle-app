const handleClick = (letter) => {
    if (!isGameOver) {
        if (letter === 'Enter') {
            checkRow()
            return
        } else if (letter === 'Del') {
            deleteLetter()
            return
        } else {
            addLetter(letter)
        }

    }

}

const addLetter = (letter) => {
    if (currentTile < 5 && currentRow < 6) {
        const tile = document.getElementById('guessRow-' + currentRow + '-tile-' + currentTile)
        tile.textContent = letter
        guessRows[currentRow][currentTile] = letter
        tile.setAttribute('data', letter)
        currentTile++
    }
}

const deleteLetter = () => {
    if (currentTile > 0) {
        currentTile--
        const tile = document.getElementById('guessRow-' + currentRow + '-tile-' + currentTile)
        tile.textContent = ''
        guessRows[currentRow][currentTile] = ''
        tile.setAttribute('data', '')
    }
}

const checkRow = () => {
    const guess = guessRows[currentRow].join('')
    if (currentTile > 4) {
        flipTile()
        if (wordle == guess ) {
            showMessage('Richtig geraten!')
            showLink()
            isGameOver = true
            return
        } else {
            if (currentRow >= 5) {
                showMessage('Leider falsch! Das Wort war ' + wordle)
                showLink()
                isGameOver = true
                return
            }
            if (currentRow < 5) {
                currentRow++
                currentTile = 0
            }
        }
    }
}

const showMessage = (message) => {
    const messageElement = document.createElement('p')
    messageElement.textContent = message
    messageDisplay.append(messageElement)
    setTimeout(() => messageDisplay.removeChild(messageElement), 100000)
}

const showLink = () => {
    const linkElement = document.createElement('a')
    linkElement.setAttribute('href', 'https://www.duden.de/suchen/dudenonline/' + wordle)
    linkElement.setAttribute('target', '_blank')
    linkElement.textContent = 'Hier klicken um das Wort zu finden'
    linkDisplay.append(linkElement)
    setTimeout(() => linkDisplay.removeChild(linkElement), 100000)
}


const addColorToKey = (letter, color) => {
    const key = document.getElementById(letter)
    key.classList.add(color)
}

const flipTile = () => {
    const rowTiles = document.querySelector('#guessRow-' + currentRow).childNodes
    let checkWordle = wordle
    const guess = []

    rowTiles.forEach(tile => {
        guess.push({ letter: tile.getAttribute('data'), color: 'grey-overlay' })
    })

    guess.forEach((guess, index) => {
        if (guess.letter == wordle[index]) {
            guess.color = 'orange-overlay'
            checkWordle = checkWordle.replace(guess.letter, '')
        }
    })

    guess.forEach(guess => {
        if (checkWordle.includes(guess.letter)) {
            guess.color = 'blue-overlay'
            checkWordle = checkWordle.replace(guess.letter, '')
        }
    })



    rowTiles.forEach((tile, index) => {
        setTimeout(() => {
            tile.classList.add('flip')
            tile.classList.add(guess[index].color)
            addColorToKey(guess[index].letter, guess[index].color)
        }, 500 * index)


    })
}



